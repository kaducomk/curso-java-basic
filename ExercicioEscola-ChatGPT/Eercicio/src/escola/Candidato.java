package escola;

public class Candidato {
    private String nome;
    private String sobrenome;
    private int idade;
    private Escolaridade nivel;

    public Candidato(String nome, String sobrenome, int idade, Escolaridade nivel) {
        this.nome = nome;
        this.sobrenome = sobrenome;
        this.idade = idade;
        this.nivel = nivel;
    }
    


}
